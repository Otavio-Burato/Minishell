#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*node;

	while (*lst)
	{
		node = *lst;
		del((*lst)->content);
		*lst = (*lst)->next;
		free(node);
	}
}
